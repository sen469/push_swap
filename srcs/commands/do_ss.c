#include "push_swap.h"

void	do_ss(t_node *st_a, t_node *st_b, int flag)
{
	if (st_a->next != st_a->prev && st_b->next != st_b->prev)
	{
		do_sa(st_a, 0);
		do_sb(st_b, 0);
		if (flag)
			write(1, "ss\n", 3);
	}
}
