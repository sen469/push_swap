int	ft_atoi(const char *str)
{
	long	num;
	int		sig;

	num = 0;
	while (*str && (*str == ' ' || (*str >= 9 && *str <= 13)))
		str++;
	sig = 1;
	if (*str == '-')
	{
		str++;
		sig = -1;
	}
	else if (*str == '+')
		str++;
	while (*str && *str >= '0' && *str <= '9')
	{
		num = num * 10 + (*str - '0');
		str++;
	}
	return ((int)sig * num);
}
