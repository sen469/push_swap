#include "libft.h"

int	ft_strncmp(const char *str1, const char *str2, size_t count)
{
	while ((*str1 || *str2) && count-- > 0)
	{
		if (*str1 != *str2)
			return (*str1 - *str2);
		str1++;
		str2++;
	}
	return (0);
}
